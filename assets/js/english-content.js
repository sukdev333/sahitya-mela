// Function to get English content for a book category
function getEnglishBookContent(category) {
    return englishBookContent[category] || englishBookContent.review;
}

// Function to load a specific chapter
function loadChapter(category, chapterIndex = 0) {
    const bookData = getEnglishBookContent(category);
    const chapter = bookData.chapters[chapterIndex];
    
    if (chapter) {
        return {
            title: chapter.title,
            content: chapter.content,
            totalChapters: bookData.chapters.length,
            currentChapter: chapterIndex + 1,
            description: bookData.description
        };
    }
    return null;
}

// Function to get all available categories
function getAvailableCategories() {
    return Object.keys(englishBookContent);
}

